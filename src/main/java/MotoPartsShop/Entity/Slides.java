package MotoPartsShop.Entity;

public class Slides {
	private int id;
	private String img;

	public Slides() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}
}
